#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	size;
	int	i;

	size = ft_strlen(s);
	i = 0;
	while (i < size)
	{
		(*f)(i, (s + i));
		i++;
	}
}
