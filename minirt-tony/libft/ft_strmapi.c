#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*str;	

	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	i = 0;
	if (!s || !f)
		return (NULL);
	if (!str)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
