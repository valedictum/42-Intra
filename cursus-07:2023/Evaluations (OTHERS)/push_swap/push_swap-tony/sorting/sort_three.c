#include "../push_swap.h"

void	sort_three(int **a, int *size_a)
{
	if ((*size_a) == 3)
	{
		if ((*a)[0] > (*a)[1] && (*a)[0] < (*a)[2])
		{
			sa(a);
		}
		else if ((*a)[0] > (*a)[1] && (*a)[1] > (*a)[2])
		{
			sa(a);
			rra(a, size_a);
		}
		else if ((*a)[0] > (*a)[1] && (*a)[1] < (*a)[2] && (*a)[0] < (*a)[2])
		{
			ra(a, size_a);
		}
		else if ((*a)[0] < (*a)[1] && (*a)[1] > (*a)[2] && (*a)[0] < (*a)[2])
		{
			sa(a);
			ra(a, size_a);
		}
		else if ((*a)[0] < (*a)[1] && (*a)[1] > (*a)[2] && (*a)[0] > (*a)[2])
		{
			rra(a, size_a);
		}
	}
}
